// Helper function to get random image for vehicle type
export const getRandomImageForVehicleType = (bodyType: string): string => {
  const lowerType = bodyType.toLowerCase();
  
  switch (lowerType) {
    case 'sedan':
      return vehicleCategories.sedan[Math.floor(Math.random() * vehicleCategories.sedan.length)];
    case 'suv':
      return vehicleCategories.suv[Math.floor(Math.random() * vehicleCategories.suv.length)];
    case 'sports':
    case 'coupe':
      return vehicleCategories.sports[Math.floor(Math.random() * vehicleCategories.sports.length)];
    case 'electric':
    case 'ev':
      return vehicleCategories.electric[Math.floor(Math.random() * vehicleCategories.electric.length)];
    case 'truck':
    case 'pickup':
      return vehicleCategories.truck[Math.floor(Math.random() * vehicleCategories.truck.length)];
    case 'van':
    case 'minivan':
      return vehicleCategories.van[Math.floor(Math.random() * vehicleCategories.van.length)];
    case 'vintage':
    case 'classic':
      return vehicleCategories.vintage[Math.floor(Math.random() * vehicleCategories.vintage.length)];
    case 'motorcycle':
    case 'bike':
      return vehicleCategories.motorcycle[Math.floor(Math.random() * vehicleCategories.motorcycle.length)];
    default:
      return demoImages.placeholder;
  }
};

