// Function to get specific image for vehicle by model name
export const getImageForVehicle = (title: string, bodyType?: string): string => {
  // First try to match by exact title
  for (const [model, image] of Object.entries(vehicleImageMappings)) {
    if (title.toLowerCase().includes(model.toLowerCase())) {
      return image;
    }
  }
  
  // If no exact match, use body type
  if (bodyType) {
    return getRandomImageForVehicleType(bodyType);
  }
  
  // Default fallback
  return demoImages.placeholder;
};

